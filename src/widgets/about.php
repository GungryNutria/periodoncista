    <h2>¿QUÉ SON LAS ENFERMEDADES PERIODONTALES?</h2>
    <p class="description">Se le llaman enfermedades periodontales a la inflamación e infección bacteriana en las encías, existen dos tipos: la gingivitis y la periodontitis. La primera es un proceso inflamatorio de las encías y se caracteriza por enrojecimiento y sangrado. La periodontitis es un agravamiento de este proceso que puede destruir los tejidos blandos y el hueso que sostiene a los dientes hasta el punto de provocar su caída. En etapas tempranas, pueden observarse signos similares a los de la gingivitis, y la pérdida de hueso podría detectarse solamente con una evaluación minuciosa por parte del periodoncista. En etapas avanzadas, puede haber movilidad dental, formación de abscesos, separación de los dientes, mal aliento y molestias para masticar. La causa principal es una mala higiene dental, lo que favorece la formación de la placa bacteriana por debajo del margen de la encía, sin embargo, algunas enfermedades como la diabetes y hábitos como el tabaquismo pueden acelerar el avance de la periodontitis.</p>
    <br>
    <h2>¿COMÓ TRATAMOS LA PERIODONTITIS?</h2>
    <p class="description">El objetivo del tratamiento de la periodontitis es resolver la inflamación y detener el daño en el hueso que rodea los dientes. En algunos casos, es posible regenerar los tejidos perdidos. La Dra. Arely López, realiza una limpieza exhaustiva alrededor de los dientes y otorga instrucciones de higiene oral de forma personalizada a cada paciente, ya que las mejores posibilidades de éxito en el tratamiento se obtienen cuando se adopta una rutina diaria de buen cuidado bucal, se controlan las afecciones de salud que pueden tener repercusión en la salud dental y se deja de fumar.</p>
